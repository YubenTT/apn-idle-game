import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const root = path.resolve(path.dirname(fileURLToPath(import.meta.url)), '../..');
const packsRoot = path.join(root, 'assets/game-packs');
const catalogPath = path.join(packsRoot, 'catalog.json');
const modulePath = path.join(root, 'js/generated/game-packs.js');

const manifestPaths = fs.readdirSync(packsRoot, { withFileTypes: true })
  .filter((entry) => entry.isDirectory())
  .map((entry) => path.join(packsRoot, entry.name, 'pack.json'))
  .filter((file) => fs.existsSync(file));

const packs = manifestPaths.map((file) => JSON.parse(fs.readFileSync(file, 'utf8')))
  .sort((a, b) => a.order - b.order || a.id.localeCompare(b.id));

const fail = (pack, message) => {
  throw new Error(`${pack?.id || 'catalog'}: ${message}`);
};

if (packs.length !== 20) fail(null, `expected 20 packs, found ${packs.length}`);
if (new Set(packs.map((pack) => pack.id)).size !== packs.length) fail(null, 'duplicate id');
if (new Set(packs.map((pack) => pack.order)).size !== packs.length) fail(null, 'duplicate order');

for (const pack of packs) {
  if (!/^[a-z0-9]+(?:-[a-z0-9]+)*$/.test(pack.id)) fail(pack, 'invalid stable id');
  if (!Number.isInteger(pack.order) || pack.order < 1) fail(pack, 'invalid order');
  if (pack.zones !== 10) fail(pack, 'zones must equal 10');
  if (!pack.genre || !pack.title) fail(pack, 'missing title or genre');
  if (!Array.isArray(pack.targets) || pack.targets.length !== 5) fail(pack, 'requires five targets');
  if (new Set(pack.targets.map((target) => target.id)).size !== 5) fail(pack, 'duplicate target id');
  if (pack.targets.some((target) => !target.role || !target.frame || !target.pivot)) fail(pack, 'incomplete target');
  if (!pack.boss?.id || !pack.boss?.breakFrame || !pack.boss?.pivot) fail(pack, 'incomplete boss');
  if (!pack.assets?.background || !pack.assets?.targets || !pack.assets?.targetData) fail(pack, 'incomplete assets');
  if (!pack.sourceBoard) fail(pack, 'missing source board');
}

const catalog = `${JSON.stringify(packs, null, 2)}\n`;
const module = `// Generated by scripts/assets/generate-catalog.mjs. Do not edit.\n` +
  `const deepFreeze = (value) => {\n` +
  `  if (value && typeof value === 'object' && !Object.isFrozen(value)) {\n` +
  `    Object.freeze(value);\n` +
  `    for (const child of Object.values(value)) deepFreeze(child);\n` +
  `  }\n` +
  `  return value;\n` +
  `};\n\n` +
  `export const GAME_PACKS = deepFreeze(${JSON.stringify(packs, null, 2)});\n\n` +
  `export const PACK_BY_ID = deepFreeze(Object.fromEntries(GAME_PACKS.map((pack) => [pack.id, pack])));\n`;

fs.mkdirSync(path.dirname(modulePath), { recursive: true });
fs.writeFileSync(catalogPath, catalog);
fs.writeFileSync(modulePath, module);
console.log(`CATALOG ${packs.length} packs`);
